async function loadMobileNet(){
  if(_mobileNetModel)return _mobileNetModel;
  if(_mobileNetLoading)return null;
  _mobileNetLoading=true;
  try{
    if(!window.tf){
      await new Promise((res,rej)=>{const s=document.createElement('script');s.src='https://cdn.jsdelivr.net/npm/@tensorflow/tfjs@4.11.0/dist/tf.min.js';s.onload=res;s.onerror=rej;document.head.appendChild(s);});
    }
    if(!window.mobilenet){
      await new Promise((res,rej)=>{const s=document.createElement('script');s.src='https://cdn.jsdelivr.net/npm/@tensorflow-models/mobilenet@2.1.0/dist/mobilenet.min.js';s.onload=res;s.onerror=rej;document.head.appendChild(s);});
    }
    _mobileNetModel=await window.mobilenet.load({version:1,alpha:0.25});
    console.log('🌿 MobileNet chargé sur l\'appareil');
    return _mobileNetModel;
  }catch(e){console.warn('MobileNet non disponible:',e.message);return null;}
  finally{_mobileNetLoading=false;}
}

async function detectTypeFromMobileNet(imgElement){
  try{
    const model=await loadMobileNet();if(!model)return null;
    const predictions=await model.classify(imgElement,5);
    for(const pred of predictions){
      const className=pred.className.toLowerCase().replace(/ /g,'_');
      for(const[key,waType]of Object.entries(MOBILENET_MAPPING)){
        if(className.includes(key))return waType;
      }
    }
    return null;
  }catch(e){console.warn('MobileNet classify error:',e);return null;}
}

