let currentLang = 'fr';
function t(key){return I18N[currentLang]?.[key]||I18N['fr']?.[key]||key;}

function initLang(){
  const saved=localStorage.getItem('wa_lang');
  if(saved&&['fr','en'].includes(saved)){currentLang=saved;}
  else{
    const b=navigator.language?.slice(0,2).toLowerCase();
    currentLang=b==='fr'?'fr':'en';
    localStorage.setItem('wa_lang',currentLang);
  }
  applyLang();
}

function setLang(lang){
  if(!['fr','en'].includes(lang))return;
  currentLang=lang;
  localStorage.setItem('wa_lang',lang);
  if(typeof track==='function')track('lang_change',{lang});
  applyLang();
  document.getElementById('lang-picker').style.display='none';
  document.getElementById('lang-check-fr').style.display=lang==='fr'?'inline':'none';
  document.getElementById('lang-check-en').style.display=lang==='en'?'inline':'none';
  toast(lang==='fr'?'🇫🇷 Français':'🇬🇧 English');
  // Persiste la langue côté profil (texte des push serveur)
  try{if(typeof me!=='undefined'&&me&&typeof sb!=='undefined'&&sb){sb.from('profiles').update({lang}).eq('id',me.id).then(()=>{},()=>{});}}catch(e){}
}

function applyLang(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key=el.getAttribute('data-i18n');
    const attr=el.getAttribute('data-i18n-attr');
    const val=t(key);
    if(attr)el.setAttribute(attr,val);
    else el.textContent=val;
  });
  // Onboarding descriptions (contain <br>, use innerHTML)
  const obDescs={
    'ob-desc-1':t('ob1_desc'),
    'ob-desc-2':t('ob2_desc'),
    'ob-desc-3':t('ob3_desc'),
  };
  Object.entries(obDescs).forEach(([id,txt])=>{
    const el=document.getElementById(id);
    if(el)el.innerHTML=txt.replace(/\n/g,'<br>');
  });
  const ld=document.getElementById('lang-display');
  if(ld)ld.textContent=currentLang==='fr'?'Français':'English';
  document.querySelectorAll('.auth-lang-btn').forEach(b=>b.classList.toggle('active',b.id===`auth-lang-${currentLang}`));
}

function openLangPicker(){
  const picker=document.getElementById('lang-picker');
  picker.style.display=picker.style.display==='block'?'none':'block';
  document.getElementById('lang-check-fr').style.display=currentLang==='fr'?'inline':'none';
  document.getElementById('lang-check-en').style.display=currentLang==='en'?'inline':'none';
}

